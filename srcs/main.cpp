volatile sig_atomic_t g_stop;

void	signalHandler(int signum)
{
	if (signum == SIGINT)
	{
		std::cout << "\n\033[1;33m‚ö†Ô∏è  Received SIGINT (Ctrl+C), shutting down...\033[0m" << std::endl;
		g_stop = 1;
	}
	else if (signum == SIGQUIT)
	{
		std::cout << "\n\033[1;33m‚ö†Ô∏è  Received SIGQUIT (Ctrl+\\), shutting down...\033[0m" << std::endl;
        g_stop = 1;
	}
	else if (signum == SIGTSTP)
		std::cout << "\n\033[1;31m‚ùå SIGTSTP (Ctrl+Z) is disabled for this server.\033[0m" << std::endl;
}

